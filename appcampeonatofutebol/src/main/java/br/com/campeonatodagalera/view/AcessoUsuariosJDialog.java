    private void jBEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEntrarActionPerformed
        try {
            // TODO add your handling code here:
            
            String usuario = jTCampoUsuario.getText();
            String senha = jPCampoSenha.getText();
            
            //Operacoes op = new Operacoes();
            user = VerificaDados.verificaLogin(usuario,senha);
            
            if( user != null ) {
                setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Dados incorretos");
                //Limpar Campos
                jTCampoUsuario.setText("");
                jPCampoSenha.setText("");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao conectar no banco.");
        }
    }//GEN-LAST:event_jBEntrarActionPerformed

