private void jComboSexo() {
        jcsexo.removeAllItems();

        for (int i = 0; i < ESexo.values().length; i++) {
            jcsexo.addItem(ESexo.values()[i].toString());
        }
    }
    private Arbitro a;

    void preparaInsert() {
        jComboSexo();

        a = new Arbitro();
    }

    void prepEdit(Arbitro arb) {
        preparaInsert();
        a = arb;
        jtnome.setText(a.getNome());
        jtnacionalidade.setText(a.getNacionalidade());
        jcsexo.setSelectedItem(a.getSexo().toString());
        jtaltura.setText(a.getAltura() + "".replace("0.0", "0.00"));
        jtdata.setText(a.getDataNascimento().toString());

    }

