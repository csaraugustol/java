//            String data = jtdata.getText().replace("/", "-");
//            a.setDataNascimento(LocalDate.parse(data));
            a.setNome(jtnome.getText());
            a.setNacionalidade(jtnacionalidade.getText());
            a.setAltura(Double.parseDouble(jtaltura.getText().replace(",", ".")));
            a.setSexo(ESexo.valueOf((String) jcsexo.getSelectedItem()));
