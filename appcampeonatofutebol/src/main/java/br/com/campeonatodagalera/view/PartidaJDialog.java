    private void jbcasdastrapartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbcasdastrapartActionPerformed
        // TODO add your handling code here:
        try {

            p.setEstadio(EEstadio.valueOf((String) jcestadio.getSelectedItem()));
            p.setHorario(jthorario.getText());
            String data = jtdata.getText().replace("/", "-");
            String[] datafort = data.split("-");
            String ano = datafort[2];
            String mes = datafort[1];
            String dia = datafort[0];
            p.setDataPartida(LocalDate.parse(ano+"-"+mes+"-"+dia));
            p.setTime1(jttime1.getText());
            p.setTime2(jttime2.getText());
            p.setNomeArb(jtnomearb.getText());
            p.setNomeCamp(jtnomecamp.getText());
            if (p.getId() == 0) {
                new PartidaDao().inserir(p);
            } else {
                new PartidaDao().alterar(p);
            }
            setVisible(false);
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar"+ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_jbcasdastrapartActionPerformed

