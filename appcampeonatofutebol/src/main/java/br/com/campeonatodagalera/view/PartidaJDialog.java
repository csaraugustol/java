    private void preencheEstadio() {
        jcestadio.removeAllItems();;
        for (int i = 0; i < EEstadio.values().length; i++) {
            jcestadio.addItem(EEstadio.values()[i].toString());
        }
    }

    private Partida p;

    void prepInsert() {
        preencheEstadio();

        p = new Partida();
    }

    void prepEdit(Partida partida) {
        prepInsert();
        p = partida;
        jttime1.setText(p.getTime1());
        jttime2.setText(p.getTime2());
        jthorario.setText(p.getHorario());
        jtdata.setText(p.getDataPartida().toString());
        jcestadio.setSelectedItem(p.getEstadio().toString());
        jtnomearb.setText(p.getNomeArb());
        jtnomecamp.setText(p.getNomeCamp());
    }

