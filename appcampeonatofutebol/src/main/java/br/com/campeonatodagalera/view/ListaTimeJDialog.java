    private ArrayList<Time> time;

    private void ListaTimes(boolean isBusca) throws ClassNotFoundException, SQLException {
        if (!isBusca) {
            time = new TimeDao().buscarTodos();
        } else {
            time = new TimeDao().buscaComFiltragem(jTnome.getText());
        }
        DefaultTableModel t = (DefaultTableModel) jTTimes.getModel();

        while (t.getRowCount() > 0) {
            t.removeRow(0);
        }

        for (Time ti : time) {

            Object[] row = {ti.getId(),ti.getNome(), ti.getCidade(), ti.getCategoria(), ti.getAnoFundacao()};

            t.addRow(row);
        }

    }
