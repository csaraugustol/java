    private void jBapagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBapagarActionPerformed
        // TODO add your handling code here:
        if (jTTimes.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Selecione o time que deseja apagar.");
        } else {
            int row = jTTimes.getSelectedRow();

            if (JOptionPane.showConfirmDialog(null, "Apagar o time " + time.get(row).getNome() + "?",
                    "Selecione", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                try {
                    new TimeDao().apagar(time.get(row));

                    ListaTimes(false);

                } catch (ClassNotFoundException | SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro ao apagar.");
                }

            }

        }
    }//GEN-LAST:event_jBapagarActionPerformed

    private void jBeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBeditActionPerformed
        // TODO add your handling code here:
        if (jTTimes.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um time.");
        } else {

            int row = jTTimes.getSelectedRow();
            //Recebe a linha
            Time t = time.get(row);

            TimeJDialog ti = new TimeJDialog(null, true);

            //Criar método para editar
            ti.prepaEdicao(t);

            ti.setVisible(true);

            //Chama lista
            try {
                new TimeDao().alterar(time.get(row));

                ListaTimes(false);

            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao preencher dados.");
            }
        }
    }//GEN-LAST:event_jBeditActionPerformed

