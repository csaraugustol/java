    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:

            tec.setNome(jtnome.getText());
            tec.setNacionalidade(jtnacionalidade.getText());
            tec.setAltura(Double.parseDouble(jtaltura.getText().replace(",", ".")));
            tec.setSexo(ESexo.valueOf((String) jcsexo.getSelectedItem()));
            String data = jtdata.getText().replace("/", "-");
            String[] datafort = data.split("-");
            String ano = datafort[2];
            String mes = datafort[1];
            String dia = datafort[0];
            tec.setDataNascimento(LocalDate.parse(ano + "-" + mes + "-" + dia));
            tec.setDataNascimento(LocalDate.parse(data));
            tec.setNumRegistro(jtnumreg.getText());
            tec.setTime(jtidtime.getText());

            if (tec.getId() == 0) {
                new TecnicoDao().inserir(tec);
            } else {
                new TecnicoDao().alterar(tec);
            }
            setVisible(false);
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar" + ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

