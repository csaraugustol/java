private void preencheSexo() {
        for (int i = 0; i < ESexo.values().length; i++) {
            jcsexo.addItem(ESexo.values()[i].toString());
        }
    }

    private Tecnico tec;

    void preparaInsert() {
        preencheSexo();

        tec = new Tecnico();
    }

    void prepEdit(Tecnico t) {
        preparaInsert();
        tec = t;
        jtnome.setText(t.getNome());
        jtnacionalidade.setText(t.getNacionalidade());
        jcsexo.setSelectedItem(t.getSexo().toString());
        jtaltura.setText(t.getAltura() + "".replace("0.0", "0.00"));
        jtdata.setText(t.getDataNascimento().toString());
        jtnumreg.setText(t.getNumRegistro());
    }

